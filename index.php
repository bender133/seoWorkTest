/**
 * @param array $someNumbers
 * @param callable $callback
 *
 * @return string
 */
function sunHelper(array $someNumbers , callable $callback): string {
  $prev = '0';
  foreach ($someNumbers as $number) {
    $prev = $callback($prev,$number);
  }
  return $prev;
}

